create table if not exists public.profiles (
  id uuid references auth.users on delete cascade primary key,
  updated_at timestamptz,
  username text,
  avatar_url text,
  diamonds int not null default 20,
  games_won int not null default 0,
  active_skin text not null default 'default',
  -- Skill tree (also applied via migration_skill_tree.sql on existing DBs)
  peak_diamonds int not null default 20,
  skill_tree jsonb not null default '{}'::jsonb
);

create table if not exists public.leaderboard (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  username text,
  max_mass int not null default 0,
  room_type text,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz
);

-- Upsert için (Flutter: leaderboard.upsert onConflict: user_id)
create unique index if not exists leaderboard_user_id_key
  on public.leaderboard (user_id);

create table if not exists public.user_skins (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete cascade not null,
  skin_id text not null,
  created_at timestamptz not null default timezone('utc', now())
);

-- Aynı kozmetiğin iki kez satın alınmasını engelle
create unique index if not exists user_skins_user_skin_key
  on public.user_skins (user_id, skin_id);

