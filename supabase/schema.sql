create or replace function public.get_user_rank(user_uuid uuid)
returns int
language plpgsql
security definer
set search_path = public
as $$
declare
  user_position int;
begin
  select position into user_position
  from (
    select
      id,
      row_number() over (order by diamonds desc, games_won desc, updated_at desc nulls last) as position
    from public.profiles
  ) ranked
  where id = user_uuid;

  return coalesce(user_position, 0);
end;
$$;

grant execute on function public.get_user_rank(uuid) to authenticated;
-- anon'a vermeyin: sıralama/PII probing yüzeyi.

